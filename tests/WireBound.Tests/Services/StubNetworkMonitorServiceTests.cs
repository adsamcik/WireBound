    [Test]
    public void Poll_SetsNonDefaultTimestamp()
    {
        // Arrange
        var before = DateTime.Now.AddSeconds(-1);

        // Act
        _service.Poll();
        var stats = _service.GetCurrentStats();

        // Assert
        stats.Timestamp.Should().BeOnOrAfter(before);
    }

    [Test]
    public void Poll_FiresStatsUpdatedEvent()
    {
        // Arrange
        NetworkStats? received = null;
        _service.StatsUpdated += (_, stats) => received = stats;

        // Act
        _service.Poll();

        // Assert
        received.Should().NotBeNull();
    }

    [Test]
    public void Poll_MultipleCalls_AccumulatesSessionBytesReceived()
    {
        // Act
        _service.Poll();
        var firstReceived = _service.GetCurrentStats().SessionBytesReceived;

        _service.Poll();
        var secondReceived = _service.GetCurrentStats().SessionBytesReceived;

        // Assert
        secondReceived.Should().BeGreaterThanOrEqualTo(firstReceived);
    }

    [Test]
    public void Poll_WithoutEventHandler_DoesNotThrow()
